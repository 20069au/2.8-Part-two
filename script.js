var showbanjoPop = true;
function banjoPopup() {
  if (showbanjoPop) {
    banjoPop.setAttribute("visible", true);
    showbanjoPop = false;
    console.log('banjo worked');
  }
  else {
    banjoPop.setAttribute("visible", false);
    showbanjoPop = true;
  }
}


